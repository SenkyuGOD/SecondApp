public class Zadacha15 {
    public static void main(String[] args) {
            double pi = Math.PI;

            System.out.println("π^1 = " + pi);
            System.out.println("π^2 = " + (pi * pi));
            System.out.println("π^3 = " + (pi * pi * pi));
            System.out.println("π^4 = " + (pi * pi * pi * pi));
    }
}
